// for loop
for (let idx = 0; idx < 5; idx++) {
  console.log("Hello");
}

// for increment
for (let idx = 0; idx < 5; idx += 4) {
  console.log("Hello");
}

// for loop array
const scores = [30, 2, 132, 12, 102];
for (let idx = 0; idx < scores.length; idx++) {
  console.log(`${idx}: ${scores[idx]}`);
}
